# Trackman Marble configuration using xinput (libinput)

# Enable Button Scroll Method
xinput set-prop "Logitech USB Trackball" "libinput Scroll Method Enabled" 0 0 1

# Scroll Button, Default is 8
# xinput set-prop "Logitech USB Trackball" "libinput Scrolling Button Default" 8

# Set Acceleration Profile, and Acceleration Speed
xinput set-prop "Logitech USB Trackball" "libinput Accel Profile Enabled" 1 0
xinput set-prop "Logitech USB Trackball" "libinput Accel Speed" 1
